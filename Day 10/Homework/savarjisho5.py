num1=int(input("enter your number: "))
num2=int(input("enter your number: "))
operation=input("which operation are you using? ")
if operation=="+"   :
    print(num1+num2)
elif operation=="-" :
    print(num1-num2)
elif operation=="*" :
    print(num1*num2)
elif operation=="/" :
    print(num1/num2)